void main(){
  // sabitler
  // final, const, Java (final), swift (let), Kotlin (val)
  int sayi = 30;
  print(sayi);
  sayi = 100;
  print(sayi);

  final int numara = 200;
  print(numara);
  // numara = 100; hata vermelidir.

  const int sonuc = 100;
  print(sonuc);
  // sonuc = 200 dersem hata verir.


  // final : runa bastığında hafızada oluşur. const: değişkeni tanımladığımızda hafızada oluşur.
  //const görsel nesnelerde kullanırız: buton text gibi.   final değişkenlerde genelde kullanılır.
  
}